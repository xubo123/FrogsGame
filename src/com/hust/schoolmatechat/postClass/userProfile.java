package com.hust.schoolmatechat.postClass;

public class userProfile {
	// 个人信息的管理
	String accountNum;
	String name;
	String phoneNum;
	String password;
	String address;
	String sign;
	String email;
	
	public userProfile(String accountNum, String name, String phoneNum,
			String password, String address, String sign, String email) {
		super();
		this.accountNum = accountNum;
		this.name = name;
		this.phoneNum = phoneNum;
		this.password = password;
		this.address = address;
		this.sign = sign;
		this.email = email;
	}





}
